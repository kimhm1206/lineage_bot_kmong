def get_help_guide_groups(
    navigation: list[dict[str, Any]],
) -> list[dict[str, Any]]:
    groups: list[dict[str, Any]] = []
    for group in navigation:
        guides: list[dict[str, Any]] = []
        for item in group["nav_items"]:
            item_id = str(item["id"])
            guide = GUIDES.get(item_id)
            if guide is None:
                continue
            guides.append(
                {
                    **item,
                    "summary": guide.summary,
                    "steps": guide.steps,
                    "tip": guide.tip,
                    "preview_title": guide.preview_title,
                    "preview_metrics": guide.preview_metrics,
                    "preview_rows": guide.preview_rows,
                    "preview_kind": GUIDE_PREVIEW_KINDS.get(item_id, "records"),
                    "access_label": GUIDE_ACCESS_LABELS.get(item_id, "유저"),
                }
            )
        if guides:
            groups.append(
                {
                    "id": group["id"],
                    "label": group["label"],
                    "icon": group["icon"],
                    "guides": guides,
                }
            )
    return groups
